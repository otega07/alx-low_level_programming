#include <stdio.h>

/**
 * add - adds up two integers
 * @t - first integer
 * @v - second integer
 * Return: addition
 */
int add(int t, int v)
{
	return (t + v);
}
/**
 * sub - subtracts two integers
 * @t - first integer
 * @v - second integer
 * Return: subtraction
 */
int sub(int t, int v)
{
	return (t - v);
}
/**
 * div - divides two integers
 * @t - first integer
 * @v - second integer
 * Return: division
 */
int div(int t, int v)
{
	return (t / v);
}
/**
 * mul - multiplies two integers
 * @t - first integer
 * @v - second integer
 * Return: multiplication
 */
int mul(int t, int v)
{
	return (t * v);
}
/**
 * mod - mod two integers
 * @t - first integer
 * @v - second integer
 * Return: final mod result
 */
int mod(int t, int v)
{
	return (t % v);
}
